#include <bits/stdc++.h>

using namespace std;
#define WORD unsigned char

WORD b[] = {0xac, 0xf3, 0xc, 0x25, 0xa3, 0x10, 0xb7, 0x25, 0x16, 0xc6, 0xb7, 0xbc, 0x7, 0x25, 0x2, 0xd5, 0xc6, 0x11, 0x7, 0xc5, 0xc};

int main() {
    for (int i = 0; i < 0x15; i++) {
        for (WORD j = 0; j < 0xFF; j++) {
            if ((WORD)(j * 0xFB) == b[i]) cout << i << ": " << j << '\n';
        }
    }
}